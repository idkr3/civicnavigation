// Helper translation function
export function tText(text, lang) {
  if (!text || lang === 'en') return text;
  const dict = translationsDict[lang];
  if (dict && dict[text]) {
    return dict[text];
  }
  
  // Custom checks for numbers and simple formatting
  if (typeof text === 'string') {
    // If it is a number or contains a suffix like "+"
    if (/^\d+\+?$/.test(text)) return text;
  }
  
  return text;
}
